let num1 = 10;

// We will multiply num1 with 5 and store the result in the same variable

// num1 = num1 * 5; // we can write this better using shorthand operators
num1 *= 5;

console.log(num1); // 50


let num2 = 5;

num2 *= 3; // 15

num2 -= 7; // 8

num2 /= 4; // 2

console.log(num2); // 2


