// assignment operator
let num1 = 10;
let num2 = 5;
let num3 = 3;
let num4 = 7;
let num5 = 2;

console.log(num1 + num5); // 12
console.log(num2 + num3 + num4); // 15

console.log(num1 - num4); // 3
console.log(num3 - num4 - num1); // -14

console.log(num5 * num3); // 6
console.log(num1 * num2 * num3 * num4 * num5); // 2100

console.log(num1 / num5); // 5
console.log(num1 / num2); // 2

// Exponentiation
console.log(num1 ** num3); // 10 ** 3 -> 10 * 10 * 10 -> 1000
console.log(num4 ** num5); // 7 ** 2 -> 7 * 7 -> 49

// Modulus
console.log(num1 % num2); // 10 % 5 -> 0
console.log(num1 % num3); // 10 % 3 -> 1
console.log(num5 % num4); // 2 % 7 -> 2

console.log(8 % 8); // 0
console.log(48 % 24); // 0
console.log((5 * 10) % 25); // 0

console.log(3 * 7 + 2); // 23
console.log(6 - 3 + 15 / 3 + 12); // 20
console.log(2 ** 3 * 8 / 16 * 2); // 8

console.log(5 - (3 % 2)); // 4
console.log((5 * 3) % 4); // 3