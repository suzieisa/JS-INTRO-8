/*
escape sequences

\n

Escape Sequences

\n inserts a new line
\t inserts a tab space
\' inserts a single quote into text
\\ inserts one backslash to your text
\" inserts a double quote into text
*/

// Output I like "Apple".

console.log("I like \"Apple\".");
console.log('I like "Apple".');

// Output I like 'Oranges'.

console.log('I like \'Oranges\'.');
console.log("I like 'Oranges'.");

/*
    JavaScript is an interpreted languages and it is used
commonly for web applications.
*/

console.log("\tJavaScript is an interpreted languages and it is used\ncommonly for web applications.");


